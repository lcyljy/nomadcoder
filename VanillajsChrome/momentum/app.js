const calculator = {
  add: function (a, b) {
    console.log(a + b);
  },
  minus: function (a, b) {
    console.log(a - b);
  },
  multi: function (a, b) {
    console.log(a * b);
  },
  div: function (a, b) {
    console.log(a / b);
  },
  powerOf: function (a, b) {
    console.log(a ** b);
  },
};

calculator.add(4, 5);
calculator.minus(4, 5);
calculator.multi(4, 5);
calculator.div(4, 5);
calculator.powerOf(4, 5);
